class WhileTest
{
	/*
	0+1
		1+2
			3+3
				6+4
					10+5
	1.每次参与假发的数值不确定
	2.每次的出现的和数据也不定
	3,。参数加法运算的值有递增规律
	4.每次都是加法运算在重复，并且都是和再加上下一个数
	
	步骤：
	1.定义一个变量，记录住参数加法运算的数据。
	2.定义一个变量，记录每次的和
	3.对参与运算的数据进行自增。
	4.因为加法运算需要重复，就要循环结构。

	//累加算法
	*/
	public static void main(String[] args)
	{
		int x=1,sum=0;
		while(x<=1000)
		{
			sum+=x;
			x++;
		}
		System.out.println("sum="+sum);
	}
}